use std::{
    env,
    error::Error,
    fs,
    path::Path,
    thread,
    time::{Duration, Instant},
};

use rayon::{ThreadPoolBuilder, prelude::*};
use serde::de::DeserializeOwned;

mod pokeapi_models;
use pokeapi_models as api;

type BuildResult<T> = Result<T, Box<dyn Error + Send + Sync>>;

const POKEAPI_POKEMON_LIST: &str = "https://pokeapi.co/api/v2/pokemon?limit=20000&offset=0";
const RETRY_ATTEMPTS: usize = 5;
const DEFAULT_HTTP_WORKERS: usize = 24;

fn main() {
    if let Err(err) = run() {
        panic!("dataset build failed: {err}");
    }
}

fn run() -> BuildResult<()> {
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=pokeapi_models.rs");

    let out_dir = env::var("OUT_DIR")?;
    let out_file = Path::new(&out_dir).join("pokemon_dataset.rs");

    let started_at = Instant::now();

    let client = reqwest::blocking::Client::builder()
        .timeout(Duration::from_secs(30))
        .build()?;

    let pokemon_links = fetch_json::<api::ResourceList>(&client, POKEAPI_POKEMON_LIST)?.results;

    let workers = env::var("POKEDAM_DATASET_HTTP_WORKERS")
        .ok()
        .and_then(|v| v.parse::<usize>().ok())
        .filter(|&v| v > 0)
        .unwrap_or(DEFAULT_HTTP_WORKERS);

    let pool = ThreadPoolBuilder::new().num_threads(workers).build()?;

    let (pokemon_data, form_data) =
        pool.install(|| fetch_pokemons_and_forms(&client, &pokemon_links))?;

    let mut pokemons = Vec::with_capacity(pokemon_data.len());
    let mut forms: Vec<String> = Vec::new();
    let mut moves: Vec<String> = Vec::new();
    let mut pokemon_types: Vec<String> = Vec::new();

    for (idx, pokemon) in pokemon_data.iter().enumerate() {
        for form in &form_data[idx] {
            forms.push(render_form(form));
        }
        let forms_end = forms.len();

        for mov in &pokemon.moves {
            let mov_id = parse_id_from_url(&mov.mov.url)
                .ok_or_else(|| format!("invalid move URL: {}", mov.mov.url))?;

            let learn_method = select_learn_method(&mov.version_group_details);

            let level_up = learn_method.as_ref().and_then(|d| {
                (d.move_learn_method.name == "level-up").then_some(d.level_learned_at)
            });

            let machine = learn_method
                .as_ref()
                .map(|d| d.move_learn_method.name == "machine")
                .unwrap_or(false);

            let level_up_value = match level_up {
                Some(level) => format!("Some({level})"),
                None => "None".to_string(),
            };

            moves.push(format!(
				"types::MovInfo {{ id: {mov_id}, learn_method: types::MovLearnMethod {{ level_up: {level_up_value}, machine: {machine} }} }}"
			));
        }
        let moves_end = moves.len();

        let mut sorted_types = pokemon.types.iter().collect::<Vec<_>>();
        sorted_types.sort_by_key(|t| t.slot);

        for ptype in sorted_types {
            let enum_variant = pokemon_type_variant(&ptype.inner.name)
                .ok_or_else(|| format!("unsupported pokemon type: {}", ptype.inner.name))?;

            pokemon_types.push(format!("types::PokemonType::{enum_variant}"));
        }
        let types_end = pokemon_types.len();

        let stats = render_stats(&pokemon.stats)?;

        pokemons.push(format!(
            "types::Pokemon {{ name: {}, forms: {forms_end}, moves: {moves_end}, types: {types_end}, stats: {stats} }}",
            rust_string(&pokemon.name)
        ));
    }

    let generated = format!(
        "// AUTO-GENERATED FILE. DO NOT EDIT BY HAND.\n\
		 // Generated by dataset/build.rs using PokeAPI.\n\n\
		 pub const POKEMONS: &[types::Pokemon; {pokemon_len}] = &[\n{pokemons_body}\n];\n\n\
		 pub const POKEMON_FORMS: &[types::PokemonForm; {forms_len}] = &[\n{forms_body}\n];\n\n\
		 pub const MOV_INFOS: &[types::MovInfo; {moves_len}] = &[\n{moves_body}\n];\n\n\
		 pub const POKEMON_TYPES: &[types::PokemonType; {types_len}] = &[\n{types_body}\n];\n",
        pokemon_len = pokemons.len(),
        forms_len = forms.len(),
        moves_len = moves.len(),
        types_len = pokemon_types.len(),
        pokemons_body = join_entries(&pokemons),
        forms_body = join_entries(&forms),
        moves_body = join_entries(&moves),
        types_body = join_entries(&pokemon_types),
    );

    fs::write(out_file, generated)?;

    println!(
        "cargo:warning=dataset generated with {} pokemon entries in {:?} ({} workers)",
        pokemons.len(),
        started_at.elapsed(),
        workers
    );

    Ok(())
}

fn fetch_pokemons_and_forms(
    client: &reqwest::blocking::Client,
    pokemon_links: &[api::Resource],
) -> BuildResult<(Vec<api::Pokemon>, Vec<Vec<api::PokemonForm>>)> {
    let mut indexed_pokemons = pokemon_links
        .par_iter()
        .enumerate()
        .map(|(idx, link)| -> BuildResult<(usize, api::Pokemon)> {
            let pokemon = fetch_json::<api::Pokemon>(client, &link.url)?;
            Ok((idx, pokemon))
        })
        .collect::<Result<Vec<_>, _>>()?;

    indexed_pokemons.sort_by_key(|(idx, _)| *idx);
    let pokemons = indexed_pokemons
        .into_iter()
        .map(|(_, pokemon)| pokemon)
        .collect::<Vec<_>>();

    let form_requests = pokemons
        .iter()
        .enumerate()
        .flat_map(|(pkm_idx, pokemon)| {
            pokemon
                .forms
                .iter()
                .enumerate()
                .map(move |(form_idx, link)| (pkm_idx, form_idx, link.url.as_str()))
        })
        .collect::<Vec<_>>();

    let mut indexed_forms = form_requests
        .par_iter()
        .map(
            |(pkm_idx, form_idx, url)| -> BuildResult<(usize, usize, api::PokemonForm)> {
                let form = fetch_json::<api::PokemonForm>(client, url)?;
                Ok((*pkm_idx, *form_idx, form))
            },
        )
        .collect::<Result<Vec<_>, _>>()?;

    indexed_forms.sort_by_key(|(pkm_idx, form_idx, _)| (*pkm_idx, *form_idx));

    let mut forms_by_pokemon = (0..pokemons.len()).map(|_| Vec::new()).collect::<Vec<_>>();
    for (pkm_idx, _form_idx, form) in indexed_forms {
        forms_by_pokemon[pkm_idx].push(form);
    }

    Ok((pokemons, forms_by_pokemon))
}

fn fetch_json<T: DeserializeOwned>(
    client: &reqwest::blocking::Client,
    url: &str,
) -> BuildResult<T> {
    for attempt in 0..RETRY_ATTEMPTS {
        match client.get(url).send() {
            Ok(response) => {
                if response.status().is_success() {
                    return Ok(response.json()?);
                }

                let status = response.status();

                if attempt + 1 == RETRY_ATTEMPTS
                    || (!status.is_server_error() && status.as_u16() != 429)
                {
                    return Err(format!("request failed for {url}: {status}").into());
                }
            }
            Err(err) => {
                if attempt + 1 == RETRY_ATTEMPTS {
                    return Err(err.into());
                }
            }
        }

        thread::sleep(Duration::from_millis(250 * (attempt as u64 + 1)));
    }

    Err("request retry loop exhausted".into())
}

fn select_learn_method(details: &[api::VersionGroupDetail]) -> Option<&api::VersionGroupDetail> {
    details
        .iter()
        .find(|d| d.version_group.name == "scarlet-violet")
        .or_else(|| {
            details
                .iter()
                .find(|d| d.version_group.name == "sword-shield")
        })
        .or_else(|| details.first())
}

fn parse_id_from_url(url: &str) -> Option<i64> {
    url.trim_end_matches('/').rsplit('/').next()?.parse().ok()
}

fn rust_string(value: &str) -> String {
    serde_json::to_string(value).expect("serializing rust string literal")
}

fn sprite_or_empty(value: Option<&String>) -> String {
    rust_string(value.map(String::as_str).unwrap_or(""))
}

fn render_form(form: &api::PokemonForm) -> String {
    let form_name = if form.form_name.trim().is_empty() {
        form.name.as_str()
    } else {
        form.form_name.as_str()
    };

    let regular_default_back = sprite_or_empty(form.sprites.back_default.as_ref());
    let regular_default_front = sprite_or_empty(form.sprites.front_default.as_ref());
    let regular_shiny_back = sprite_or_empty(form.sprites.back_shiny.as_ref());
    let regular_shiny_front = sprite_or_empty(form.sprites.front_shiny.as_ref());

    let has_female = form.sprites.back_female.is_some()
        || form.sprites.front_female.is_some()
        || form.sprites.back_shiny_female.is_some()
        || form.sprites.front_shiny_female.is_some();

    let female = if has_female {
        format!(
            "Some(types::PokeSpritesInner {{ default: types::PokeSprite {{ back: {}, front: {} }}, shiny: types::PokeSprite {{ back: {}, front: {} }} }})",
            sprite_or_empty(form.sprites.back_female.as_ref()),
            sprite_or_empty(form.sprites.front_female.as_ref()),
            sprite_or_empty(form.sprites.back_shiny_female.as_ref()),
            sprite_or_empty(form.sprites.front_shiny_female.as_ref()),
        )
    } else {
        "None".to_string()
    };

    format!(
        "types::PokemonForm {{ name: {}, sprites: types::PokeSprites {{ regular: types::PokeSpritesInner {{ default: types::PokeSprite {{ back: {regular_default_back}, front: {regular_default_front} }}, shiny: types::PokeSprite {{ back: {regular_shiny_back}, front: {regular_shiny_front} }} }}, female: {female} }} }}",
        rust_string(form_name)
    )
}

fn render_stats(stats: &[api::Stat]) -> BuildResult<String> {
    let mut total_hp: Option<u16> = None;
    let mut attack: Option<u16> = None;
    let mut defense: Option<u16> = None;
    let mut special_attack: Option<u16> = None;
    let mut special_defense: Option<u16> = None;
    let mut speed: Option<u16> = None;

    for stat in stats {
        match stat.stat.name.as_str() {
            "hp" => total_hp = Some(stat.base_stat),
            "attack" => attack = Some(stat.base_stat),
            "defense" => defense = Some(stat.base_stat),
            "special-attack" => special_attack = Some(stat.base_stat),
            "special-defense" => special_defense = Some(stat.base_stat),
            "speed" => speed = Some(stat.base_stat),
            _ => {}
        }
    }

    let total_hp = total_hp.ok_or("missing hp stat")?;
    let attack = attack.ok_or("missing attack stat")?;
    let defense = defense.ok_or("missing defense stat")?;
    let special_attack = special_attack.ok_or("missing special-attack stat")?;
    let special_defense = special_defense.ok_or("missing special-defense stat")?;
    let speed = speed.ok_or("missing speed stat")?;

    Ok(format!(
        "types::Stats {{ total_hp: {total_hp}, attack: {attack}, defense: {defense}, special_attack: {special_attack}, special_defense: {special_defense}, speed: {speed} }}"
    ))
}

fn pokemon_type_variant(name: &str) -> Option<&'static str> {
    match name {
        "normal" => Some("Normal"),
        "fighting" => Some("Fighting"),
        "flying" => Some("Flying"),
        "poison" => Some("Poison"),
        "ground" => Some("Ground"),
        "rock" => Some("Rock"),
        "bug" => Some("Bug"),
        "ghost" => Some("Ghost"),
        "steel" => Some("Steel"),
        "fire" => Some("Fire"),
        "water" => Some("Water"),
        "grass" => Some("Grass"),
        "electric" => Some("Electric"),
        "psychic" => Some("Psychic"),
        "ice" => Some("Ice"),
        "dragon" => Some("Dragon"),
        "dark" => Some("Dark"),
        "fairy" => Some("Fairy"),
        "stellar" => Some("Stellar"),
        _ => None,
    }
}

fn join_entries(entries: &[String]) -> String {
    entries
        .iter()
        .map(|entry| format!("    {entry},"))
        .collect::<Vec<_>>()
        .join("\n")
}
